def test_collect_writes_a_snapshot_to_stdout(capsys, monkeypatch) -> None:
    monkeypatch.setattr(cli, "connect", lambda dsn: FakeConnection(catalog_rows()))
    code, out, _ = run(capsys, "collect", "--dsn", "postgresql:///fleet")
    payload = json.loads(out)
    assert code == EXIT_OK
    assert payload["tables"][0]["name"] == "public.pings"


def test_collect_writes_a_snapshot_to_a_file(capsys, monkeypatch, tmp_path: Path) -> None:
    monkeypatch.setattr(cli, "connect", lambda dsn: FakeConnection(catalog_rows()))
    target = tmp_path / "catalog.json"
    code, _, err = run(
        capsys, "collect", "--dsn", "postgresql:///fleet", "--output", str(target)
    )
    assert code == EXIT_OK
    assert "Wrote snapshot" in err
    assert json.loads(target.read_text(encoding="utf-8"))["tables"]


def test_collect_reports_an_unwritable_output_path(capsys, monkeypatch) -> None:
    monkeypatch.setattr(cli, "connect", lambda dsn: FakeConnection(catalog_rows()))
    code, _, err = run(
        capsys, "collect", "--dsn", "postgresql:///fleet", "-o", "/nonexistent/dir/out.json"
    )
    assert code == EXIT_ERROR
    assert "cannot write" in err


def test_collect_surfaces_connection_failures(capsys, monkeypatch) -> None:
    from spatial_index_advisor.errors import CollectorError

    def explode(dsn: str):
        raise CollectorError("cannot connect to the database: no route to host")

    monkeypatch.setattr(cli, "connect", explode)
    code, _, err = run(capsys, "collect", "--dsn", "postgresql:///nope")
    assert code == EXIT_ERROR
    assert "no route to host" in err
