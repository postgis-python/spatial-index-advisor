def test_clean_workload_exits_zero(capsys, tmp_path: Path) -> None:
    catalog = tmp_path / "catalog.json"
    catalog.write_text(
        json.dumps(
            {
                "tables": [
                    {
                        "name": "public.t",
                        "row_count": 100,
                        "table_bytes": 8192,
                        "geometry_columns": [{"name": "geom", "geometry_type": "POINT"}],
                    }
                ]
            }
        ),
        encoding="utf-8",
    )
    workload = tmp_path / "q.sql"
    workload.write_text("SELECT 1 FROM t WHERE ST_Intersects(geom, $1);", encoding="utf-8")
    code, out, _ = run(capsys, "analyze", "-w", str(workload), "-c", str(catalog), "--no-color")
    assert code == EXIT_OK
    assert "No recommendations" in out


